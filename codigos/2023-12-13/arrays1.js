var numeros = [];

numeros.push(10); //adiciona um elemento no final da array
console.log(numeros);
numeros.push(11);
numeros.push(13);
console.log(numeros);
numeros.pop(); //remove o último elemento
console.log(numeros);
numeros.unshift(14); //adiciona na primeira posição da array
console.log(numeros);
numeros.shift(); //remove o primeiro elemento
console.log(numeros);
console.log(numeros.length); //retorna o tamanho da array
